    private final int pixel = 20;

    public int[][] selectedMaze;
    public int cakesLeft;
    public int totalCakes;
    public boolean gameOver;

    public Maze(int lvl) {

        if (lvl == 1) {
            selectedMaze = maze1;
        } else {
            selectedMaze = maze2;
        }
        int total = 0;
        for (int i = 0; i < selectedMaze.length; i++) {

            for (int j : selectedMaze[i]) {
                if (j == 1) {
                    total++;
                }
            }

        }
        cakesLeft = total;
        totalCakes = total;
        gameOver = false;

    }

    public void draw(Canvas canvas) {

        double pixelSize = canvas.getWidth() / pixel;
        GraphicsContext gc = canvas.getGraphicsContext2D();
        gc.setFill(Color.LIGHTBLUE);
        gc.fillRect(0, 0, canvas.getWidth(), canvas.getHeight());
        for (int i = 0; i < pixel; i++) {

            for (int j = 0; j < pixel; j++) {

                if (selectedMaze[i][j] == 0) {

                    gc.setFill(Color.BLACK);
                    gc.fillRect(j * pixelSize, i * pixelSize, pixelSize, pixelSize);

                } else if (selectedMaze[i][j] == 1) {

                    gc.setFill(Color.WHITE);
                    gc.fillOval(j * pixelSize, i * pixelSize, pixelSize, pixelSize);

                }

            }

        }

    }

    public void removeCake(Canvas canvas, int x, int y) {

        double pixelSize = canvas.getWidth() / pixel;
        GraphicsContext gc = canvas.getGraphicsContext2D();
        gc.setFill(Color.LIGHTBLUE);
        gc.fillOval(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
        selectedMaze[y][x] = 4;
        if (canvas.getUserData().equals("board")) {
            cakesLeft--;
        }

    }

    public void clearMaze(Canvas canvas) {

        selectedMaze = maze1;
        canvas.getGraphicsContext2D().clearRect(0, 0, canvas.getWidth(), canvas.getHeight());
        cakesLeft = 0;

    }

